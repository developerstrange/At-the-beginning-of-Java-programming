package codeWithHarry_package;

public class _62_SixtySecondClassWithHarry {

	public static void main(String[] args) {
		System.out.println("Interpreted vs Compiled Languages!");
		
		/*
		 	Interpreter translates one statement at a time into machine code
		 	compiler scans the entire program and translates whole of it into machine code
		 	
		 	interpreter-->
		 	1. one statement at a time
		 	2. interpreter is needed every-time
		 	3. partial execution if error occurs
		 	4. easy to use for programmers
		 	5. Slower than compiled languages
		 	6.Py-thon, Java-Script etc. 
		 	
		 	Compiler-->
		 	1. entire program at a time
		 	2. once compiled it is not needed
		 	3. no execution if an error occurs
		 	4. usually not as easy as interpreted languages
		 	5. Faster than interpreted languages
		 	6. C, C++ etc.
		 */

	}

}
